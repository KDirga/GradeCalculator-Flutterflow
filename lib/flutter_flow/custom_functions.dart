String? determineLetterGrade(int? score) {
  if (score == null) {
    return "Cannot be null";
  }

  if (score < 0 || score > 100) {
    return "Out of range";
  }

  if (score >= 90) {
    return "A";
  } else if (score >= 80) {
    return "B";
  } else if (score >= 70) {
    return "C";
  } else if (score >= 60) {
    return "D";
  } else {
    return "F";
  }
}
